// ランダム選択関数（例）
function getRandomItem(array) {
    return array[Math.floor(Math.random() * array.length)];
}

function getRandomRoulette() {
    const bind = getRandomItem(binds);
    let result = { bind: bind };

    switch (bind) {
        case "国縛り":
            result.value = getRandomItem(subRoulettes["国縛り"]);
            break;
        case "初期キャラのみ":
            result.value = getRandomItem(subRoulettes["初期キャラのみ"]);
            break;
        case "キャラルーレット":
            result.value = getRandomItem(subRoulettes["キャラルーレット"]);
            break;
        case "武器種縛り":
            result.value = getRandomItem(subRoulettes["武器種縛り"]);
            break;
        case "モノ元素縛り":
            result.value = getRandomItem(subRoulettes["モノ元素縛り"]);
            break;
        case "各1.1縛り":
            result.value = getRandomItem(subRoulettes["各1.1縛り"]);
            break;
        case "誕生月":
            result.value = getRandomItem(subRoulettes["誕生月"]);
            break;
        case "アルファベット縛り":
            result.value = getRandomItem(subRoulettes["アルファベット縛り"]);
            break;
        default:
            result.value = null;
    }

    return result;
}

